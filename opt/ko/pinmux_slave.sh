#!/bin/sh

echo "run $0 begin!!!";

#级联VO输出口
himm 0x120F01D4 0x1; # 0: GPIO0_0    1:VOU_SLV_DAT15
himm 0x120F01D8 0x1; # 0: GPIO0_1    1:VOU_SLV_DAT1
himm 0x120F01DC 0x1; # 0: GPIO0_2    1:VOU_SLV_DAT0
himm 0x120F01E0 0x1; # 0: GPIO0_3    1:VOU_SLV_DAT9
himm 0x120F01E4 0x1; # 0: GPIO0_4    1:VOU_SLV_DAT14
himm 0x120F01E8 0x1; # 0: GPIO0_5    1:VOU_SLV_DAT8
himm 0x120F01EC 0x1; # 0: GPIO0_6    1:VOU_SLV_CLK
himm 0x120F01F0 0x1; # 0: GPIO0_7    1:VOU_SLV_DAT2
himm 0x120F01F4 0x1; # 0: GPIO13_7   1:VOU_SLV_DAT3
himm 0x120F01F8 0x1; # 0: UART0_RXD  1:VOU_SLV_DAT5     2:GPIO18_0
himm 0x120F01FC 0x1; # 0: UART0_TXD  1:VOU_SLV_DAT4     2:GPIO18_1
himm 0x120F0200 0x1; # 0: GPIO18_2   1:VOU_SLV_DAT11    2:UART1_RTSN   3:UART0_RTSN
himm 0x120F0204 0x1; # 0: GPIO18_3   1:VOU_SLV_DAT10    2:UART1_CTSN   3:UART0_CTSN
himm 0x120F0208 0x1; # 0: GPIO18_4   1:VOU_SLV_DAT7     2:UART1_RXD
himm 0x120F020C 0x1; # 0: GPIO18_5   1:VOU_SLV_DAT6     2:UART1_TXD
himm 0x120F0210 0x1; # 0: GPIO18_6   1:VOU_SLV_DAT12    2:UART2_RXD    3:UART0_RTSN
himm 0x120F0214 0x1; # 0: GPIO18_7   1:VOU_SLV_DAT13    2:UART2_TXD    3:UART0_CTSN

#UART0
himm 0x120F0158 0x2;   # 0: GPIO9_0   1:VOU1120_DAT0    2:UART0_RXD
himm 0x120F015C 0x2;   # 0: GPIO9_1   1:VOU1120_DAT1    2:UART0_TXD
himm 0x120F0160 0x3;   # 0: GPIO9_2   1:VOU1120_DAT2    2:UART2_RXD     3:UART0_RTSN
himm 0x120F0164 0x3;   # 0: GPIO9_3   1:VOU1120_DAT3    2:UART2_TXD     3:UART0_CTSN

echo "run $0 end!!!";